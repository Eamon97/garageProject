package com.qa.garage;

public abstract class Vehicle {
	public String make;
	public String model;
	public int productionYear;
	
	
	public Vehicle(String make, String model, int productionYear) {
		super();
		this.make = make;
		this.model = model;
		this.productionYear = productionYear;
	}



	
	

	
	

}
